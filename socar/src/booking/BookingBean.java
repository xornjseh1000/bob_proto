package booking;

public class BookingBean {
	private boolean travel;
	String rentDate, returnDate, region, rentZone, returnZone, img, model;

	public boolean isTravel() {
		return travel;
	}

	public void setTravel(boolean travel) {
		this.travel = travel;
	}

	public String getRentDate() {
		return rentDate;
	}

	public void setRentDate(String rentDate) {
		this.rentDate = rentDate;
	}

	public String getReturnDate() {
		return returnDate;
	}

	public void setReturnDate(String returnDate) {
		this.returnDate = returnDate;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getRentZone() {
		return rentZone;
	}

	public void setRentZone(String rentZone) {
		this.rentZone = rentZone;
	}

	public String getReturnZone() {
		return returnZone;
	}

	public void setReturnZone(String returnZone) {
		this.returnZone = returnZone;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}
	
	
}